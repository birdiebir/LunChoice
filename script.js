function openGate() {
  if (auth.user) return;
  // openGate() 被很多地方防呆呼叫，只有真的從關到開那一刻才搶焦點，
  // 不然使用者可能正在填密碼，卻被之後又觸發的 openGate() 打斷。
  const wasOpen = $("gate").classList.contains("show");
  openVeil("gate");
  if (!wasOpen) $("gateEmail").focus();
  if (!CONFIGURED) {
    setGateMsg("尚未設定 Supabase：請在 index.html 頂端填入 SUPABASE_URL 與 ANON KEY", "err");
    $("gateSubmit").disabled = true;
  }
}
function closeGate() { closeVeil("gate"); }
function setGateMsg(text, kind) {
  const m = $("gateMsg");
  m.textContent = text;
  m.className = "g-msg" + (kind ? " " + kind : "");
}

async function applySession(session) {
  auth.user = session?.user ?? null;
  if (auth.user) {
    closeGate();
    $("authBar").hidden = false;
    updateWhoDisplay();
    await fetchStatus();
    loadPersonalSpots();
    subscribePersonalSpots();
    loadUserOrigins();
    loadMyProfile();
    subscribeGroupResults();
    if (!groupAutoActivateDone) {
      groupAutoActivateDone = true;
      autoActivateGroupOnLogin();
    } else if (activeGroup) {
      switchGroupSpots(activeGroup.group_id);
    }
  } else {
    auth.remaining = null;
    $("authBar").hidden = true;
    myProfile = { nickname: null, avatar_url: null };
    updateAvatarThumb();
    activeGroup = null;
    currentGroupDetail = null;
    activeGroupDetail = null;
    groupAutoActivateDone = false;
    myGroups = [];
    myGroupsLoaded = false;
    groupSpotsGroupId = null;
    sharedSpots = [];
    personalSpots = [];
    userOrigins = [];
    // 登出後沒人在看畫面了，訂閱還留著只是白白一直收 Realtime 事件、
    // 觸發不必要的重繪（issue #023）；下次登入 subscribeSharedSpots()／
    // subscribeGroupResults() 的「已有訂閱就 return」防呆會自然重建。
    if (sharedChannel) { supabase.removeChannel(sharedChannel); sharedChannel = null; }
    if (personalChannel) { supabase.removeChannel(personalChannel); personalChannel = null; }
    if (groupResultChannel) { supabase.removeChannel(groupResultChannel); groupResultChannel = null; }
    syncGroupBanner();
    updateQuota();
    refresh();
    openGate();
  }
}

function readGateInput() {
  const email = $("gateEmail").value.trim();
  const password = $("gatePassword").value;
  if (!email || !email.includes("@")) { setGateMsg("請輸入正確的 email", "err"); return null; }
  if (!password || password.length < 6) { setGateMsg("密碼至少需要 6 碼", "err"); return null; }
  return { email, password };
}
async function withGateBusy(busyText, fn) {
  $("gateSubmit").disabled = true;
  setGateMsg(busyText, "");
  try { await fn(); }
  finally { $("gateSubmit").disabled = false; }
}
$("gateSubmit").onclick = async () => {
  const cred = readGateInput();
  if (!cred) return;
  await withGateBusy("處理中…", async () => {
    const { error: loginError } = await supabase.auth.signInWithPassword(cred);
    if (!loginError) return; // 登入成功，onAuthStateChange 會自動帶進場
    const { data, error: signUpError } = await supabase.auth.signUp(cred);
    if (!signUpError) {
      if (!data.session) setGateMsg("帳號已建立，但信箱驗證尚未關閉，請到 Supabase 後台關閉 Confirm email", "err");
      return; // 有 session 的話 onAuthStateChange 會自動帶進場
    }
    if (/registered|exists/i.test(signUpError.message)) setGateMsg("密碼錯誤，請再試一次", "err");
    else setGateMsg(signUpError.message, "err");
  });
};
$("gatePassword").addEventListener("keydown", e => { if (e.key === "Enter") $("gateSubmit").click(); });
$("gateEmail").addEventListener("keydown", e => { if (e.key === "Enter") $("gatePassword").focus(); });
