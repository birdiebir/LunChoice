function segColor(i, n) {
  if (n > 1 && i === n - 1 && i % PALETTE.length === 0) return PALETTE[3];
  return PALETTE[i % PALETTE.length];
}
function drawWheel(list) {
  ctx.clearRect(0, 0, S, S);
  const n = list.length;
  if (n === 0) return;
  const seg = 2 * Math.PI / n;
  const fs = n <= 10 ? 40 : n <= 18 ? 33 : n <= 28 ? 28 : 24;
  const maxChars = n <= 10 ? 9 : n <= 18 ? 8 : n <= 28 ? 7 : 6;

  for (let i = 0; i < n; i++) {
    const a0 = state.rot + i * seg, a1 = a0 + seg;
    ctx.beginPath();
    ctx.moveTo(CX, CX);
    ctx.arc(CX, CX, R, a0, a1);
    ctx.closePath();
    ctx.fillStyle = segColor(i, n);
    ctx.fill();
    ctx.strokeStyle = "rgba(255,255,255,.9)";
    ctx.lineWidth = n > 60 ? 1.5 : 3;
    ctx.stroke();

    if (n <= 36) {
      let label = list[i].name;
      if (label.length > maxChars) label = label.slice(0, maxChars - 1) + "…";
      ctx.save();
      ctx.translate(CX, CX);
      ctx.rotate(a0 + seg / 2);
      ctx.textAlign = "right";
      ctx.textBaseline = "middle";
      ctx.fillStyle = TEXT_ON_WHEEL;
      ctx.font = `700 ${fs}px "Microsoft JhengHei","PingFang TC","Noto Sans TC",sans-serif`;
      ctx.fillText(label, R - 26, 0);
      ctx.restore();
    }
  }
  ctx.beginPath();
  ctx.arc(CX, CX, R, 0, 2 * Math.PI);
  ctx.strokeStyle = "rgba(0,0,0,.14)";
  ctx.lineWidth = 8;
  ctx.stroke();
}

