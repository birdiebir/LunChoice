async function refreshActiveGroupStatus() {
  if (!activeGroup) return;
  try {
    const { data, error } = await supabase.rpc("group_status", { p_group_id: activeGroup.group_id });
    if (!error && data && data.ok) {
      activeGroupDetail = data;
    } else if (!error && data && !data.ok) {
      // 已經不在這個群組了（被圈主移除、或群組因為圈主退出而被刪除）：
      // 回到個人模式，不要讓畫面繼續卡在舊的「今天輪到你」狀態，甚至
      // 因此誤放行一次其實不該發生的轉盤（issue #004）。
      activeGroup = null;
      activeGroupDetail = null;
      switchGroupSpots(null);
      if (state.wheelMode !== "personal") { state.wheelMode = "personal"; syncWheelModeUI(); }
      setHint("你已經不在這個群組了，已切回個人轉盤", true);
    }
  } catch (e) {}
  syncGroupBanner();
  refresh();
}

/* 建立新群組／主動加入群組後，直接把它設成使用中的群組轉盤，等同使用者
   自己按了一次「用這個群組轉盤」——不用再多一步手動切換。 */
async function activateGroup(groupId, name) {
  activeGroup = { group_id: groupId, name };
  groupBannerExpanded = false;
  switchGroupSpots(groupId);
  await refreshActiveGroupStatus();
}

/* 有加入飯搭子圈的話，登入當下就自動綁定群組轉盤（不用每次都手動點「用這個
   群組轉盤」）；只有使用者自己按「離開群組模式」才會回到個人模式——見
   groupAutoActivateDone 的註解，同一次登入只會自動綁定這一次。多個群組時
   挑 my_groups() 回傳的第一筆（依 created_by 時間排序，也就是最早加入的
   那個），維持結果穩定可預期。 */
async function autoActivateGroupOnLogin() {
  try {
    const { data, error } = await supabase.rpc("my_groups");
    if (!error && Array.isArray(data)) myGroups = data;
  } catch (e) {}
  myGroupsLoaded = true;
  if (activeGroup || myGroups.length === 0) { refresh(); return; }
  const g = myGroups[0];
  activeGroup = { group_id: g.group_id, name: g.name };
  switchGroupSpots(g.group_id);
  await refreshActiveGroupStatus();
}

let groupResultChannel = null;
function subscribeGroupResults() {
  if (groupResultChannel) return;
  groupResultChannel = supabase.channel("group_spin_results_changes")
    .on("postgres_changes", { event: "INSERT", schema: "public", table: "group_spin_results" }, payload => {
      if (activeGroup && payload.new.group_id === activeGroup.group_id) refreshActiveGroupStatus();
      if (currentGroupDetail && payload.new.group_id === currentGroupDetail.group_id) refreshGroupDetail(currentGroupDetail.group_id);
    })
    .subscribe();
}

