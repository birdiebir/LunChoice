const reduceMotion = matchMedia("(prefers-reduced-motion: reduce)").matches;

/* 對外的入口：先向伺服器登記一次「轉盤」，成功（未超過每日上限）才播放動畫。
   次數的真正把關在 Supabase 端，前端擋不住也改不掉。 */
async function spin() {
  if (state.spinning || auth.busy || currentList.length === 0) return;
  if (!CONFIGURED) { openGate(); return; }
  if (!auth.user) { openGate(); return; }
  if (state.wheelMode !== "personal" && activeGroup && !isMyGroupTurn()) { setHint("今天不是輪到你，等輪到你再轉吧 🍚", true); return; }
  if (outOfSpins()) return;

  auth.busy = true;
  refresh();
  /* 記住這次「登記轉盤」當下實際看到的清單：recordSpin() 這段 await 期間
     （共享轉盤模式下）可能有人透過 Realtime 新增/刪除地點，觸發 refresh()
     把全域 currentList 換掉，甚至變空。次數已經是照這份清單登記的，動畫跟
     結果就該對應這份清單，不然會出現「次數扣了，畫面卻完全沒反應」。 */
  const spinList = currentList;
  const res = await recordSpin();
  auth.busy = false;

  if (!res || !res.ok) {
    if (res && res.error === "limit_reached") {
      auth.remaining = 0;
      updateQuota();
    } else {
      setHint("⚠️ 連線發生問題，請稍後再試", true);
    }
    refresh();
    return;
  }

  auth.remaining = res.remaining;
  updateQuota();
  animateSpin(res.spin_id, spinList);
}

function animateSpin(spinId, list) {
  if (state.spinning || list.length === 0) return;
  const n = list.length;
  const winner = Math.floor(Math.random() * n);
  const seg = 2 * Math.PI / n;
  const pointerAngle = -Math.PI / 2;
  const t = 0.2 + Math.random() * 0.6;
  const base = pointerAngle - (winner + t) * seg;
  const minTurns = reduceMotion ? 1 : 5;
  const k = Math.ceil((state.rot + minTurns * 2 * Math.PI - base) / (2 * Math.PI));
  const target = base + k * 2 * Math.PI;

  const start = state.rot;
  const dist = target - start;
  const dur = reduceMotion ? 500 : 4400;
  const t0 = performance.now();
  state.spinning = true;
  $("spinBtn").disabled = true;
  $("brand").classList.add("spinning");

  function frame(now) {
    const p = Math.min(1, (now - t0) / dur);
    const ease = 1 - Math.pow(1 - p, 4);
    state.rot = start + dist * ease;
    drawWheel(list);
    if (p < 1) {
      requestAnimationFrame(frame);
    } else {
      state.rot = ((target % (2 * Math.PI)) + 2 * Math.PI) % (2 * Math.PI);
      state.spinning = false;
      $("brand").classList.remove("spinning");
      refresh();   // 依登入狀態與剩餘次數重新決定按鈕是否可按
      updateQuota();  // 轉盤停下後才決定是否顯示「看廣告多轉一次」，避免蓋住轉動畫面
      showResult(list[winner]);
      recordSpinOutcome(list[winner], spinId);
    }
  }
  requestAnimationFrame(frame);
}

/* 轉盤動畫結束、知道轉到誰之後才回填：個人紀錄 + （如果在群組模式）廣播給全組。
   都是背景動作，不影響畫面上已經顯示的結果，但失敗時要讓使用者知道
   （issue #002）——不然次數已經扣了，畫面卻悄悄跟後端狀態脫節。 */
async function recordSpinOutcome(winner, spinId) {
  try {
    if (spinId) {
      const { error } = await supabase.rpc("set_spin_result", {
        p_spin_id: spinId, p_winner_name: winner.name, p_wheel_mode: state.wheelMode
      });
      if (error) throw error;
    }
  } catch (e) {
    setHint("⚠️ 結果同步失敗，個人紀錄可能沒存到，請重新整理確認", true);
  }
  // 個人轉盤轉出來的結果是私人的，不廣播給群組——不然會被誤當成「今天
  // 全組吃的」貼到群組看板上。只有飯搭子轉盤本身的結果才廣播。
  if (activeGroup && state.wheelMode === "shared") {
    try {
      const { error } = await supabase.rpc("record_group_result", {
        p_group_id: activeGroup.group_id, p_winner_name: winner.name, p_wheel_mode: state.wheelMode
      });
      if (error) throw error;
    } catch (e) {
      setHint("⚠️ 群組結果廣播失敗，其他成員可能看不到，請重新整理確認", true);
    }
    // 不管廣播成功與否都要重新讀一次群組狀態，讓畫面反映後端目前的
    // 實際狀態，而不是繼續停在「輪到你」的舊資訊。
    await refreshActiveGroupStatus();
  }
}

