function armOrRemoveGroupMember(btn, groupId, userId) {
  if (btn.dataset.armed === "1") {
    removeGroupMember(groupId, userId);
    return;
  }
  btn.dataset.armed = "1";
  btn.textContent = "確定？";
  btn.classList.add("confirm");
  setTimeout(() => {
    if (btn.isConnected) {
      btn.dataset.armed = "0";
      btn.textContent = "×";
      btn.classList.remove("confirm");
    }
  }, 2500);
}

